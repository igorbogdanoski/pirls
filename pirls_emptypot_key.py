from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

def create_pirls_emptypot_key():
    doc = Document()
    doc.styles['Normal'].font.name = 'Arial'
    doc.styles['Normal'].font.size = Pt(12)
    doc.styles['Normal'].paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE

    doc.add_heading('УПАТСТВО ЗА ОЦЕНУВАЊЕ (КЛУЧ): Празната саксија', 0)

    mcq = [("1", "б"), ("3", "а"), ("6", "в"), ("7", "б"), ("8", "г"), ("11", "а"), ("12", "в"), ("13", "в"), ("14", "г"), ("15", "б")]
    doc.add_paragraph("ПРАШАЊА СО ПОВЕЌЕЧЛЕН ИЗБОР (1 бод)").runs[0].bold = True
    for q, a in mcq:
        doc.add_paragraph(f"Прашање {q}: Точен одговор {a}")

    open_q = [
        ("2", "Што добиле децата", "1 бод: Во одговорот е наведено дека Царот на секое дете му дал семе / варено семе / драгоцена можност."),
        ("4", "Најдобар градинар", "1 бод: Идентификува доказ од приказната. Пр. „Тој ги одгледуваше најдобрите растенија“ или „Соседите сакаа да земаат зеленчук од него“."),
        ("9", "Зошто децата се смееле", "2 бода: Демонстрира разбирање на контрастот: неговото семе не пораснало, додека нивните пораснале (иако тој бил најдобар градинар)."),
        ("10", "Советот на родителите", "1 бод: Покажува разбирање дека тие сакале тој да биде искрен / да преземе одговорност / да биде горд што дал сè од себе."),
        ("16", "Зошто Царот се насмевнал", "1 бод: Покажува разбирање дека Јун победил / дека Царот ја препознал неговата искреност (не мамел)."),
        ("17", "Чувствата на Јун", "Надежен (1 бод): се надевал на својата вештина да го одгледа семето.\nЗбунет (1 бод): и покрај неговата грижа, неговото растение не пораснало.\nПоразен (1 бод): сфатил дека и покрај напорите, нема шанса да победи со празна саксија.")
    ]

    doc.add_paragraph("\nПРАШАЊА ОД ОТВОРЕН ТИП").runs[0].bold = True
    for q, name, criteria in open_q:
        p = doc.add_paragraph()
        p.add_run(f"Прашање {q} ({name}): ").bold = True
        doc.add_paragraph(criteria)

    doc.save("PIRLS_Key_EmptyPot_Nastavnik.docx")

if __name__ == "__main__":
    create_pirls_emptypot_key()
