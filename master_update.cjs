for (const [storyId, data] of Object.entries(updates)) {
  const storyRegex = new RegExp(`${storyId}:\\s*{[\\s\\S]*?questions:\\s*\\[([\\s\\S]*?)\\]`, 'g');
  const match = storyRegex.exec(content);
  if (match) {
    let questionsContent = match[1];
    data.questions.forEach(qUpdate => {
      // Find question object by ID
      const qRegex = new RegExp(`{\\s*id:\\s*${qUpdate.id},[\\s\\S]*?}`);
      const qMatch = qRegex.exec(questionsContent);
      if (qMatch) {
        let qObj = qMatch[0];
        // Inject criteria and hint
        if (!qObj.includes('criteria:')) {
          qObj = qObj.replace(/}\s*$/, `, criteria: ${JSON.stringify(qUpdate.criteria)}, hint: ${JSON.stringify(qUpdate.hint)} }`);
          questionsContent = questionsContent.replace(qMatch[0], qObj);
        }
      }
    });
    // Replace back into content
    content = content.replace(match[1], questionsContent);
  }
}

