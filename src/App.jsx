const Timer = ({ active }) => {
  const [seconds, setSeconds] = React.useState(0);

  React.useEffect(() => {
    let interval = null;
    if (active) {
      interval = setInterval(() => {
        setSeconds(s => s + 1);
      }, 1000);
    } else {
      clearInterval(interval);
    }
    return () => clearInterval(interval);
  }, [active]);

  const formatTime = (s) => {
    const mins = Math.floor(s / 60);
    const secs = s % 60;
    return `${mins}:${secs.toString().padStart(2, '0')}`;
  };

  return (
    <div className="flex items-center gap-2 bg-amber-50 px-4 py-2 rounded-2xl border-2 border-amber-200 text-amber-700 font-black">
      <span className="text-xl">⏱️</span>
      <span className="font-mono text-lg">{formatTime(seconds)}</span>
    </div>
  );
};

const TextWithGlossary = ({ content, glossary, onTermClick }) => {
  if (!content) return null;
  const terms = Object.keys(glossary);
  if (terms.length === 0) return content;
  
  const regex = new RegExp(`(${terms.join('|')})`, 'gi');
  const parts = content.split(regex);
  
  return (
    <>
      {parts.map((part, i) => {
        const lowerPart = part.toLowerCase();
        if (glossary[lowerPart]) {
          return (
            <span 
              key={i} 
              onClick={() => onTermClick(lowerPart, glossary[lowerPart])}
              className="cursor-help border-b-2 border-dashed border-indigo-400 text-indigo-700 font-bold hover:bg-indigo-50 transition-colors px-1 rounded-md"
            >
              {part}
            </span>
          );
        }
        return part;
      })}
    </>
  );
};

