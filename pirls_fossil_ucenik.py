from docx import Document
from docx.shared import Pt, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

def create_pirls_test():
    doc = Document()
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1)
        section.right_margin = Inches(1)

    style = doc.styles['Normal']
    style.font.name = 'Arial'
    style.font.size = Pt(14)
    style.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE

    # Title
    t = doc.add_heading('МИСТЕРИЈАТА НА ЏИНОВСКИОТ ЗАБ', 0)
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph("Фосилот претставува остатоци од кое било суштество или растение што живеело на Земјата многу, многу години одамна. Луѓето наоѓаат фосили со илјадници години во карпите и гребените и покрај езерата. Денес знаеме дека некои од овие фосили се фосили од диносауруси.")
    doc.add_paragraph("Многу одамна, луѓето што наоѓале огромни фосили не знаеле што се тие. Некои мислеле дека големите коски потекнуваат од големи животни што тие ги виделе или за коишто читале, како што се нилските коњи или слоновите. Но, некои од коските што луѓето ги наоѓале биле преголеми за да потекнуваат дури и од најголемиот нилски коњ или слон. Овие огромни коски навеле некои луѓе да веруваат во џинови.")
    
    doc.add_paragraph("Пред стотици години, во Франција, еден човек што се викал Бернар Палиси добил друга идеја. Тој бил прочуен грнчар. Кога ги изработувал своите грниња, тој наоѓал голем број мали фосили во глината. Тој ги проучувал фосилите и напишал дека тие биле остатоци од живи суштества. Ова не била нова идеја. Но, Бернар Палиси напишал, исто така, дека некои од овие суштества повеќе не живеат на Земјата. Тие исчезнале целосно. Тие биле изумрени.")
    doc.add_paragraph("Дали Бернар Палиси бил награден за своето откритие? Не! Тој бил ставен во затвор поради неговите идеи. Со текот на времето, некои луѓе станале поотворени кон новите идеи за тоа како светот би можел да изгледа многу одамна.")
    
    # Illustration 1
    p1 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Фармер наоѓа огромен фосил во поле ]")
    p1.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph("Потоа, во 1820-тите години, во Англија бил пронајден огромен фосил од заб. Се смета дека Мери Ен Мантел, сопругата на Гидеон Мантел, експерт за фосили, била излезена на прошетка кога видела нешто што личи на огромен камен заб. Мери Ен Мантел знаела дека големиот заб е фосил и го однела дома кај својот сопруг.")
    doc.add_paragraph("Кога Гидеон Мантел го видел фосилниот заб за првпат, тој мислел дека му припаѓал на некое тревопасно животно, бидејќи забот бил сплеснат и имал гребени. Забот бил истрошен од џвакање храна. Тој бил голем речиси колку заб од слон. Сепак, воопшто не наликувал на заб од слон.")
    
    doc.add_paragraph("Гидеон Мантел можел да забележи дека парчињата од карпата што биле споени со забот биле многу стари. Тој знаел дека станува збор за вид на карпа во која се наоѓале фосили од влекачи. Дали можеби забот му припаѓал на џиновски тревопасен влекач што ја џвакал својата храна? Некој вид влекач што повеќе не живее на Земјата?")
    doc.add_paragraph("Гидеон Мантел бил вистински зачуден од големиот заб. Ниеден влекач за којшто тој знаел не ја џвакал храната. Влекачите ја голтаат својата храна и затоа нивните заби не се трошат. Тоа била мистерија.")
    
    # Illustration 2
    p2 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Гидеон Мантел го проучува забот со лупа ]")
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph("Гидеон Мантел го однел забот во еден музеј во Лондон и им го покажал на други научници. Никој не се согласил со Гидеон Мантел дека тоа би можело да биде заб од џиновски влекач. Гидеон Мантел се обидел да најде некој влекач што имал заб кој наликувал на џиновскиот заб. Долго време тој не успеал да најде ништо. Потоа еден ден се запознал со некој научник што ги проучувал игуаните.")
    doc.add_paragraph("Научникот му покажал на Гидеон Мантел заб од игуана. Конечно! Ова бил забот од жив влекач што изгледал како мистериозниот заб. Само што фосилниот заб бил многу, многу поголем. Сега Гидеон Мантел верувал дека фосилниот заб му припаѓал на некое животно што изгледало како игуана. Гидеон Мантел сметал дека тоа било долго стотина стапки! Ова суштество го нарекол Игуанодон. Името значи „заб од игуана“.")
    
    doc.add_paragraph("Гидеон Мантел го немал целиот скелет од Игуанодон. Меѓутоа, од коските што ги собрал со текот на времето, тој се обидел да открие како би можел да изгледа Игуанодонот. Тој сметал дека коските покажуваат дека суштеството се движело на сите четири нозе. Тој мислел дека една зашилена коска била рог. Тој нацртал Игуанодон со рог на носот.")
    doc.add_paragraph("Години подоцна биле најдени неколку целосни скелети од Игуанодон. Коските покажувале дека животното одело на задните нозе дел од времето. Исто така, се покажало дека она што Гидеон Мантел сметал дека е рог на неговиот нос всушност е шилец на неговиот палец!")
    
    # Illustration 3
    p3 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Споредба на два цртежи на Игуанодон - стар и нов ]")
    p3.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph("Гидеон Мантел направил неколку грешки. Сепак, тој имал и значајно откритие. По неговата првична идеја дека фосилниот заб му припаѓал на тревопасен влекач, тој потрошил многу години собирајќи факти и докази за да докаже дека неговите идеи биле точни. Гидеон Мантел бил еден од првите луѓе што покажале дека многу одамна на Земјата живееле џиновски влекачи. И потоа тие изумреле.")
    doc.add_paragraph("Стотици години претходно, Бернар Палиси бил осуден на затвор затоа што го тврдел истото. Но, Гидеон Мантел станал славен. Во 1842 година научникот Ричард Овен ги нарекол овие изумрени влекачи Диносауриа. Ова значи „ужасно голем гуштер“. Денес ги нарекуваме диносауруси.")

    doc.add_page_break()
    doc.add_heading('ПРАШАЊА ЗА ТЕКСТОТ', 1)

    questions = [
        "1. Што е тоа фосил?",
        "2. Според статијата, зошто некои луѓе одамна верувале во џинови?",
        "3. Каде Бернар Палиси ги наоѓал фосилите?",
        "4. Што била новата идеја на Бернар Палиси?",
        "5. Зошто Бернар Палиси бил ставен во затвор?",
        "6. Кој го нашол фосилниот заб во Англија?",
        "7. Што е она што Гидеон Мантел го знаел за влекачите што направило фосилниот заб да биде збунувачки?",
        "8. Пополни ја табелата: Зошто Мантел мислел дека забот е од тревопасно животно (сплеснат заб), а зошто дека е од влекач?",
        "9. Зошто Гидеон Мантел го однел забот во музеј?",
        "10. Зошто забот од игуана бил важен за Гидеон Мантел?",
        "11. Што користел Гидеон Мантел кога се обидувал да открие како изгледал Игуанодонот?",
        "12. Што ни покажуваат цртежите на Игуанодонот во текстот?",
        "13. Наведете две нешта што науката денес ги знае за Игуанодонот, а Гидеон Мантел не ги знаел.",
        "14. Што било откриено и покажало дека Гидеон не бил во право за изгледот на Игуанодонот?",
        "15. Што направил Гидеон Мантел за да докаже дека неговите идеи биле точни?"
    ]

    for q in questions:
        p = doc.add_paragraph()
        p.add_run(q).bold = True
        if "Што е тоа фосил" in q or "Каде Бернар" in q or "Зошто Бернар" in q or "Кој го нашол" in q or "Што е она што Гидеон" in q or "Зошто Гидеон" in q or "Што користел" in q or "Што било откриено" in q:
            doc.add_paragraph("   А. ________   Б. ________   В. ________   Г. ________")
        else:
            doc.add_paragraph("_" * 60)
            doc.add_paragraph("_" * 60)

    doc.save("PIRLS_Test_Fossil_Ucenik.docx")

if __name__ == "__main__":
    create_pirls_test()
