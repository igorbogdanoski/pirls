        "Баба Гун пред тоа цел живот живеела во таа селска куќичка, но никогаш не се чувствувала осамена, бидејќи имала многу животни со кои се забавувала: крава, седум кокошки, две овци и една мачка.",
        "Еден ден Баба Гун се разболела. „Ти не си сериозно болна, но треба да се преселиш в град,“ - ѝ рекол докторот. „Не е многу паметно да живееш овде сосема сама. Твојата крава не може да ме повика ако скршиш нога во дворот!“",
        "„Можам да се грижам за себе!“ - одговорила Баба Гун. Меѓутоа, тогаш си помислила дека можеби ќе биде забавно да се живее и в град. „Во ред!“ - рекла одеднаш таа. „Ќе се преселам во градот.“",
