        "Баба Гун ги спакувала сите свои работи во еден камион и набргу била на пат кон својот нов дом. Била многу возбудена и навистина се радувала што ќе го види градот.",
        "И јас, исто така, бев многу возбуден! Не можев да дочекам да видам кој ќе се всели до станот спроти нашиот. Меѓутоа, тоа беше Баба Гун. Сепак, во најмала рака таа имаше мачка.",
        "Баба Гун не беше премногу среќна кога го разгледа својот нов стан. „Па, ова е ужасно!“ - рече таа. „Сите ѕидови се мазни и бели. А погледни ги и прозорциве! Тие се премногу големи!“ Потоа стана многу молчелива. „Се враќам дома!“ - рече таа, и се сврте да си оди.",
        "Тогаш таа списка. Роберт, мачката, туку што беше скокнала низ прозорецот! „Не грижи се.“ – ѝ реков брзо. „Таа само скокна на балконот. Погледни!“",
        "Баба Гун јурна покрај мене на балконот. Но, кога стигна тамy, заборави за Роберт. Балконот беше огромен и можеше да ги гледа планините што се протегаа далеку напред, па дури и дел од морето. Баба Гун клекна, така што не можеше да види ниту еден од врвовите на покривите – освен планините и небото. Но, и покрај сè Баба Гун реши да остане.",
        "Следниот ден кога ја посетив да ѝ помогнам да се распакува, таа сè уште изгледаше многу несреќна. „Дали си нерасположена бидејќи твоите животни се толку далеку?“ - ја прашав. „Повеќе би рекла дека ми недостигаат,“ - воздивна таа. „Па, тогаш зошто не одиш таму и да си ги донесеш?“ - ја прашав.",
        "Баба Гун ми намигна и го искриви лицето. Следниот ден, кога отидов да ја посетам дома, таму немаше никој. Баба Гун се беше качила на автобус и тргнала за в село.",
