from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

def create_lambe_key():
    doc = Document()
    doc.styles['Normal'].font.name = 'Arial'
    doc.styles['Normal'].font.size = Pt(12)
    doc.styles['Normal'].paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE

    doc.add_heading('УПАТСТВО ЗА ОЦЕНУВАЊЕ (КЛУЧ): Дедо Ламбе и глувците', 0).alignment = WD_ALIGN_PARAGRAPH.CENTER

    mcq = [("1", "Б"), ("2", "Г"), ("3", "Б"), ("5", "В"), ("8", "В"), ("9", "А"), ("13", "В")]
    doc.add_paragraph("ПРАШАЊА СО ПОВЕЌЕЧЛЕН ИЗБОР (1 бод)").runs[0].bold = True
    for q, a in mcq:
        doc.add_paragraph(f"Прашање {q}: Точен одговор {a}")

    open_q = [
        ("4", "Зошто се насмеал", "1 бод: Тој не бил изненаден бидејќи имал подобар план или сакал глувците да мислат дека тој е глупав."),
        ("6", "Каде мислеле дека стојат", "2 бода: Вклучува докази за ДВАТА елементи: 1) мислеле дека стојат на таванот (висат наопаку) И 2) одлучиле да застанат на глава."),
        ("7", "Реченица за паника", "1 бод: Ја препишува (делумно/целосно) реченицата како: „Ова е страшно! Ох, леле мајко!“ или „Ќе паднам во несвест...“"),
        ("10", "Каде ги ставил", "1 бод: Во (плетена) кошница."),
        ("11", "Дали е лесно да се излажат", "1 бод: Дава став (Да/Не) И соодветна причина базирана на текстот (Пр. Да: поверуваа дека таванот е под; Не: првата ноќ знаеја за стапиците)."),
        ("12", "Каков е Ламбе", "3 бода: Опишува карактерна особина (итар, паметен, трпелив) И дава ДВА примери за активности (пр. ги остави стапиците да ги излаже; го залепи мебелот на таван)."),
        ("14", "Неверојатна приказна", "2 бода: Опишува нереален аспект И објаснува зошто не е можно во реалноста (Пр. Глувците зборуваат, а животните не зборуваат човечки јазик; или лепило не може да држи телевизор на таван).")
    ]

    doc.add_paragraph("\nПРАШАЊА ОД ОТВОРЕН ТИП").runs[0].bold = True
    for q, name, criteria in open_q:
        p = doc.add_paragraph()
        p.add_run(f"Прашање {q} ({name}): ").bold = True
        doc.add_paragraph(f"• {criteria}")

    doc.save("PIRLS_Key_Dedo_Lambe_Nastavnik.docx")

create_lambe_key()
