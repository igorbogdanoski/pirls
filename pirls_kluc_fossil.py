from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH

def create_pirls_key():
    doc = Document()
    doc.add_heading('УПАТСТВО ЗА ОЦЕНУВАЊЕ (КЛУЧ): Мистеријата на џиновскиот заб', 0)
    
    style = doc.styles['Normal']
    style.font.name = 'Arial'
    style.font.size = Pt(12)

    # MCQ
    mcqs = [
        ("1", "В (1 бод)"),
        ("3", "Б (1 бод)"),
        ("5", "А (1 бод)"),
        ("6", "Б (1 бод)"),
        ("7", "Г (1 бод)"),
        ("9", "В (1 бод)"),
        ("11", "А (1 бод)"),
        ("14", "Г (1 бод)")
    ]
    
    doc.add_heading('ПРАШАЊА СО ПОВЕЌЕЧЛЕН ИЗБОР', level=1)
    for q, a in mcqs:
        doc.add_paragraph(f"Прашање {q}: {a}")

    # Open-ended
    open_qs = [
        ("2", "Џинови", "1 бод: Бидејќи наоѓале огромни коски/скелети што биле премногу големи за познати животни."),
        ("4", "Новата идеја на Палиси", "1 бод: Фосилите биле од животни што изумреле / повеќе не живеат на Земјата."),
        ("8", "Зошто тревопасно/влекач", "1 бод за тревопасно (сплеснат заб), 1 бод за влекач (тип на карпа ИЛИ сличен на заб од игуана)."),
        ("10", "Заб од игуана", "1 бод: Доказ за неговата теорија дека фосилот е од џиновски влекач."),
        ("12", "Цртежи на Игуанодон", "2 бода: Покажуваат како се промениле научните идеи / грешките што ги направил Мантел."),
        ("13", "Наука денес", "1 бод за секое точно (одел на две нозе, шилец на палецот, долг 30 стапки). Макс 2 бода."),
        ("15", "Докажување идеи", "1 бод: Потрошил многу години собирајќи факти и докази.")
    ]

    doc.add_heading('ПРАШАЊА ОД ОТВОРЕН ТИП', level=1)
    for q, title, criteria in open_qs:
        p = doc.add_paragraph()
        p.add_run(f"Прашање {q}: {title}").bold = True
        doc.add_paragraph(criteria)

    doc.save("PIRLS_Kluc_Fossil_Nastavnik.docx")

if __name__ == "__main__":
    create_pirls_key()
