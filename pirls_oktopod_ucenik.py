from docx import Document
from docx.shared import Pt, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.text import WD_LINE_SPACING

def setup_document():
    doc = Document()
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1)
        section.right_margin = Inches(1)

    style = doc.styles['Normal']
    font = style.font
    font.name = 'Arial'
    font.size = Pt(14)
    style.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    style.paragraph_format.space_after = Pt(12)
    return doc

def add_title(doc, text, level=1):
    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER if level == 1 else WD_ALIGN_PARAGRAPH.LEFT
    run = title.add_run(text)
    run.font.name = 'Arial'
    run.font.size = Pt(16 if level == 1 else 14)
    run.bold = True

def add_paragraph(doc, text):
    doc.add_paragraph(text).alignment = WD_ALIGN_PARAGRAPH.JUSTIFY

def add_image_prompt(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(f"[ МЕСТО ЗА ИЛУСТРАЦИЈА: {text} ]")
    run.font.size = Pt(12)
    run.italic = True
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER

def add_question_mcq(doc, number, question, options):
    doc.add_paragraph(f"{number}. {question}").runs[0].bold = True
    for option in options:
        doc.add_paragraph(f"O  {option}").paragraph_format.left_indent = Inches(0.5)

def add_question_checkbox(doc, number, question, options):
    doc.add_paragraph(f"{number}. {question}").runs[0].bold = True
    for option in options:
        doc.add_paragraph(f"[   ]  {option}").paragraph_format.left_indent = Inches(0.5)

def add_question_open(doc, number, question, lines=2):
    doc.add_paragraph(f"{number}. {question}").runs[0].bold = True
    for _ in range(lines):
        doc.add_paragraph("_" * 60)

