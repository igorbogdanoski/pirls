from docx import Document
from docx.shared import Pt, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

def create_pirls_test():
    doc = Document()
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1)
        section.right_margin = Inches(1)

    style = doc.styles['Normal']
    style.font.name = 'Arial'
    style.font.size = Pt(14)
    style.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE

    # Title
    t = doc.add_heading('ОТКРИЈТЕ ЈА ЗАБАВАТА ВО ДНЕВНОТО ПЕШАЧЕЊЕ', 0)
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Intro
    doc.add_paragraph("Барате нешто забавно и интересно што би го правеле дома или на одмор? Еден од најдобрите начини за уживање во природата е да пешачите и дневното пешачење е најпопуларната од овие активности. За него не се потребни многу време и каква било посебна опрема. Дневното пешачење е забавно и претставува добра вежба!")
    
    # Illustration placeholder 1
    p1 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Група деца пешачат на сончева патека во шума ]")
    p1.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_heading('Вие сте надлежни!', level=1)
    doc.add_paragraph("Вие може да изберете каде сакате да одите, колку долго сакате да бидете надвор и колку брзо сакате да одите. Можете едноставно полека да шетате и да уживате во природата или пак да си поставите предизвик совладувајќи потешки и стрми пешачки патеки. Сè зависи од вас!")

    doc.add_heading('Видете нови интересни нешта!', level=1)
    doc.add_paragraph("Пешачењето може да ве однесе на места што не би можеле да ги видите поинаку. Може да одите во прекрасни области со спектакуларни глетки. Или, пак, може да одите во некои оддалечени области каде што може да има скриени долини, водопади или пештери. Пешачењето може да ви даде можност да видете растенија, птици и животни што живеат во дивината. Би можеле дури и да видете остатоци од згради и предмети што им припаѓале на луѓето што живееле таму многу одамна.")
    
    # Illustration placeholder 2
    p2 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Скриен водопад во карпеста долина со диви цвеќиња ]")
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_heading('Планирајте ја вашата дневна прошетка', level=1)
    doc.add_paragraph("• Изберете некое забавно и интересно место каде што ќе одите.\n• Откријте го растојанието и утврдете колку време би требало да ви одземе.\n• Проверете ги временските услови и прогнозата.\n• Не пакувајте многу работи. Она што треба да го носите не треба да биде премногу тешко.")

    doc.add_heading('Список за проверка за пакување', level=1)
    doc.add_paragraph("• Многу вода – за да не ожедните\n• Храна – енергетски ужинки или ручек за пикник\n• Прибор за прва помош – во случај на драсканици\n• Дополнителен пар чорапи – би можеле да ги наводените нозете\n• Свирче – важно е ако ви е потребна помош")
    
    # Illustration placeholder 3
    p3 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Опрема за пешачење: шише вода, прва помош, чорапи и свирче ]")
    p3.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_heading('Како да бидете безбедни на вашето дневно пешачење', level=1)
    doc.add_paragraph("1. Тргнете рано за да се вратите пред да се стемни.\n2. Останете на патеките за пешачење освен ако не ја познавате областа.\n3. Одете со умерена брзина. Не пешачете пребргу за да ја зачувате енергијата.\n4. Внимавајте на животинскиот свет. Никогаш не им се приближувајте на животните во дивината. Тие можеби изгледаат слатко, но може да бидат непредвидливи и многу заштитнички настроени.")
    
    # Illustration placeholder 4
    p4 = doc.add_paragraph("[ ИЛУСТРАЦИЈА: Животни во дивина ]")
    p4.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_heading('Истражете го Стражарскиот рид (Легенда за мапата)', level=1)
    doc.add_paragraph("1. Прошетка за набљудување птици | 2 часа | Лесно, пристап за инвалидска количка\n2. Пешачење до стражарската станица | 2 до 2,5 часа во еден правец | Тешко\n3. Патека до Жабји поток | 3 часа | Средно | Пешачете до излетничката област Жабји поток\n4. Круг околу Стражарски рид | 5 часа | Средно | Пешачете околу Стражарски рид")

    doc.add_page_break()
    doc.add_heading('ПРАШАЊА ЗА ТЕКСТОТ', 1)

    questions = [
        "1. Која е главната порака што брошурата ви ја пренесе за пешачењето?",
        "2. Наведете две интересни нешта што брошурата наведува дека би можеле да ги видите при дневното пешачење.",
        "3. Кои две нешта според брошурата треба да ги имате предвид кога пешачите во група?",
        "4. Кој дел од брошурата ви соопшти да носите соодветна облека за временските услови?",
        "5. Зошто треба да земете уште еден пар чорапи кога одите на пешачење?",
        "6. Што треба да сторите ако западнете во неволја додека сте на пешачење?",
        "7. Што треба да направите за да избегнете да се преморите пребргу?",
        "8. Зошто е важно да кажете некому кога планирате да се вратите од пешачењето?",
        "9. Која маршрута би ја избрале ако сакате да одите на најкратката прошетка?",
        "10. Каков вид луѓе би биле најспособни за пешачењето на Стражарски рид?",
        "11. Кои две нешта може да ги научите ако ја проучите легендата на мапата?",
        "12. Искористете ја легендата за мапата на Стражарски рид за да го испланирате пешачењето. Која маршрута би ја избрале?",
        "13. Наведете две причини од брошурата зошто ја избирате оваа маршрута.",
        "14. Според текстот, зошто никогаш НЕ треба да им се приближувате на животните во дивината, дури и ако изгледаат слатко и безопасно?",
        "15. Дали мислиш дека оваа брошура е корисна за некој што никогаш претходно не бил на пешачење? Објасни го твојот одговор користејќи пример од текстот."
    ]

    for q in questions:
        p = doc.add_paragraph()
        p.add_run(q).bold = True
        if "Која е главната порака" in q or "Кој дел од брошурата" in q or "Зошто треба да земете" in q or "Што треба да сторите" in q or "Што треба да направите" in q or "Која маршрута" in q or "Каков вид луѓе" in q:
            doc.add_paragraph("   А. ________   Б. ________   В. ________   Г. ________")
        else:
            doc.add_paragraph("_" * 60)
            doc.add_paragraph("_" * 60)

    doc.save("PIRLS_Test_Hiking_Ucenik.docx")

if __name__ == "__main__":
    create_pirls_test()
