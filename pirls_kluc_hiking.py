from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH

def create_pirls_key():
    doc = Document()
    doc.add_heading('УПАТСТВО ЗА ОЦЕНУВАЊЕ (КЛУЧ): Дневно пешачење', 0)
    
    style = doc.styles['Normal']
    style.font.name = 'Arial'
    style.font.size = Pt(12)

    # MCQ
    mcqs = [
        ("1", "В (1 бод)"),
        ("4", "Б (1 бод)"),
        ("5", "А (1 бод)"),
        ("6", "Б (1 бод)"),
        ("7", "В (1 бод)"),
        ("9", "А (1 бод)"),
        ("10", "Г (1 бод)")
    ]
    
    doc.add_heading('ПРАШАЊА СО ПОВЕЌЕЧЛЕН ИЗБОР', level=1)
    for q, a in mcqs:
        doc.add_paragraph(f"Прашање {q}: {a}")

    # Open-ended
    open_qs = [
        ("2", "Наведете две нешта", "1 бод: Растенија, птици, животни, водопади, пештери, тврдини, глетки."),
        ("3", "Пешачење во група", "2 бода: Способност (темпо) И интереси на сите членови. 1 бод само за едно од тие."),
        ("8", "Зошто се кажува некому", "1 бод: За да знаат дека нешто не е во ред ако не се вратите и да најдат помош."),
        ("11", "Легенда на мапата", "2 бода: Вклучува кои било две точни информации (време, тежина, опис). 1 бод за една."),
        ("12/13", "Планирање", "2 бода: Го наведува изборот и дава две логични причини од текстот. 1 бод за една причина."),
        ("14", "Животни во дивина", "1 бод: Затоа што се непредвидливи ИЛИ заштитнички настроени."),
        ("15", "Корисност", "2 бода: Евалуација (Да) + пример (пр. списокот за пакување му помага на почетникот). 1 бод за општ одговор.")
    ]

    doc.add_heading('ПРАШАЊА ОД ОТВОРЕН ТИП', level=1)
    for q, title, criteria in open_qs:
        p = doc.add_paragraph()
        p.add_run(f"Прашање {q}: {title}").bold = True
        doc.add_paragraph(criteria)

    doc.save("PIRLS_Kluc_Hiking_Nastavnik.docx")

if __name__ == "__main__":
    create_pirls_key()
