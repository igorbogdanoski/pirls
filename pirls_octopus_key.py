from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING

def create_octopus_key():
    doc = Document()
    doc.styles['Normal'].font.name = 'Arial'
    doc.styles['Normal'].font.size = Pt(12)
    doc.styles['Normal'].paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE

    doc.add_heading('УПАТСТВО ЗА ОЦЕНУВАЊЕ (КЛУЧ): Прекрасниот октопод', 0).alignment = WD_ALIGN_PARAGRAPH.CENTER

    mcq = [("3", "А (Можат да изгледаат како нешто друго)"), 
           ("4", "В (Се сместуваат во многу тесни места)"), 
           ("8", "Б (Да поместува лостови за да прска боја на платно)"), 
           ("13", "В (Посегнуваат и да се припојуваат до рацете на нивните чувари)")]
    doc.add_paragraph("ПРАШАЊА СО ПОВЕЌЕЧЛЕН ИЗБОР (1 бод)").runs[0].bold = True
    for q, a in mcq:
        doc.add_paragraph(f"Прашање {q}: Точен одговор {a}")

    open_q = [
        ("1", "Точни искази (Штиклирање)", "2 бода: Штиклирани се САМО: Имаат округли тела; Имаат осум долги пипала; Сакаат да јадат ракови и мали риби."),
        ("2", "Врати", "1 бод: Во одговорот е наведено дека користат карпи."),
        ("5", "Невообичаени места", "1 бод: Даден е еден од примерите: опрема на научници, шишиња, или школки."),
        ("6", "Бегање од грабливци", "2 бода: Наведени се ДВА начина (се кријат во дувла/пукнатини/тесни места; се кријат зад школки; брзи пливачи; испуштаат мастило; ја менуваат бојата; создаваат точки/дамки; прават камени „врати“)."),
        ("7", "Фрида", "1 бод: Научила сама да ги отвора теглите со храна."),
        ("9", "Добри слики", "1 бод: Да (уметничките дела се продаваат за пари) ИЛИ Не (авторот ги ставил „уметничките дела“ во наводници)."),
        ("10", "Сложувалки", "1 бод: Бидејќи им е здодевно, па им даваат сложувалки за да ги занимаваат."),
        ("11", "Играчка на Семи", "1 бод: Топка или пластична топка."),
        ("12", "Покажуваат дека се среќни", "2 бода: Наведени се ДВЕ од следниве: Стануваат црвени; Стојат високо на пипалата / се наведнуваат нанапред; Скокаат на задните „нозе“ и мавтаат; Се припојуваат околу рацете на чуварот."),
        ("13", "Компатибилност со чувари", "1 бод: Посегнуваат и се припојуваат до рацете на нивните чувари."),
        ("14", "Три примери", "3 бода: Наведени се ТРИ: Решаваат загатки (сложувалки); Отвораат тегли; Сликаат (цртаат); Си играат; Одвојуваат и составуваат топка; Ги препознаваат/поздравуваат чуварите."),
        ("15", "Аквариумите се добри", "1 бод: Да (нахранети се; добиваат играчки; учат; безбедни се; ги сакаат чуварите) ИЛИ Не (здодевно им е; треба да бидат слободни; сакаат сами да ловат).")
    ]

    doc.add_paragraph("\nПРАШАЊА ОД ОТВОРЕН ТИП").runs[0].bold = True
    for q, name, criteria in open_q:
        p = doc.add_paragraph()
        p.add_run(f"Прашање {q} ({name}): ").bold = True
        doc.add_paragraph(criteria)

    doc.save("PIRLS_Key_Octopus_Nastavnik.docx")
    print("Teacher key generated!")

create_octopus_key()
